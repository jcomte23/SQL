/*
SELECT
Es una de las cláusulas fundamentales en SQL. 
Se utiliza para recuperar datos de una o más tablas de una base de datos
*/

-- Obtiene todos los datos de la tabla "users"
SELECT * FROM users;

-- Obtiene todos los nombres de la tabla "users"
SELECT nombres FROM users;

-- Obtiene todos los nombres y apellidos de la tabla "users"
SELECT nombres, apellidos FROM users;